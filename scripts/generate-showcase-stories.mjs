import { mkdir, rm, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const marker = 'Generated by scripts/generate-showcase-stories.mjs';

const storyKindExportNames = {
  default: 'Default',
  variants: 'Variants',
  sizes: 'Sizes',
  states: 'States',
  examples: 'Examples',
};

const registries = [
  {
    library: 'mantine',
    titlePrefix: 'Mantine',
    source: resolve(root, 'src/showcase/mantine-showcase.tsx'),
    exportName: 'mantineShowcaseEntries',
    idPrefix: 'mantine-',
  },
  {
    library: 'daisyui',
    titlePrefix: 'daisyUI',
    source: resolve(root, 'src/showcase/daisyui-showcase.tsx'),
    exportName: 'daisyUiShowcaseEntries',
    idPrefix: 'daisyui-',
  },
];

function componentName(name) {
  const cleaned = name.replace(/[^A-Za-z0-9_$]/g, '');
  const normalized = cleaned[0]?.toUpperCase() + cleaned.slice(1);
  return `${normalized || 'Showcase'}Story`;
}

function storyTitleName(name) {
  return name[0]?.toUpperCase() + name.slice(1);
}

async function readText(path) {
  return await import('node:fs/promises').then(({ readFile }) =>
    readFile(path, 'utf8'),
  );
}

function extractEntries(sourceText) {
  return [
    ...sourceText.matchAll(
      /id: '([^']+)',\s+name: '([^']+)'([\s\S]*?)(?=\n\s+render:)/g,
    ),
  ].map(([, id, name, metadata]) => {
    const storyKindsMatch = metadata.match(/storyKinds:\s*\[([^\]]+)\]/);
    const storyKinds = storyKindsMatch
      ? [...storyKindsMatch[1].matchAll(/'([^']+)'/g)].map(([, storyKind]) => storyKind)
      : ['default'];

    return { id, name, storyKinds };
  });
}

function storySource({ entry, registry }) {
  const storyComponent = componentName(entry.name);
  const titleName = storyTitleName(entry.name);
  const options = entry.storyKinds.map((storyKind) => `'${storyKind}'`).join(', ');
  const storyExports = entry.storyKinds
    .map(
      (storyKind) => `export const ${storyKindExportNames[storyKind]}: Story = {
  args: {
    storyKind: '${storyKind}',
  },
};`,
    )
    .join('\n\n');

  return `// ${marker}\nimport '../../../src/showcase/showcase.css';\nimport type { Meta, StoryObj } from '@storybook/react-vite';\nimport type { ShowcaseStoryKind } from '../../../src/showcase/index.js';\nimport {\n  ${registry.exportName},\n  SingleShowcaseStory,\n} from '../../../src/showcase/index.js';\n\ntype ComponentStoryProps = {\n  storyKind?: ShowcaseStoryKind;\n};\n\nconst entry = ${registry.exportName}.find(\n  (showcaseEntry) => showcaseEntry.id === '${entry.id}',\n);\n\nif (!entry) {\n  throw new Error('Missing showcase entry: ${entry.id}');\n}\n\nfunction ${storyComponent}({ storyKind = 'default' }: ComponentStoryProps) {\n  return <SingleShowcaseStory entry={entry} library="${registry.library}" storyKind={storyKind} />;\n}\n\nconst meta = {\n  title: '${registry.titlePrefix}/${titleName}',\n  component: ${storyComponent},\n  tags: ['autodocs'],\n  args: {\n    storyKind: 'default',\n  },\n  argTypes: {\n    storyKind: {\n      control: 'select',\n      options: [${options}],\n      description: 'Switch between component stories.',\n    },\n  },\n  parameters: {\n    layout: 'fullscreen',\n    docs: {\n      description: {\n        component: entry.description,\n      },\n    },\n  },\n} satisfies Meta<typeof ${storyComponent}>;\n\nexport default meta;\n\ntype Story = StoryObj<typeof meta>;\n\n${storyExports}\n`;
}

for (const registry of registries) {
  const sourceText = await readText(registry.source);
  const entries = extractEntries(sourceText);
  const outDir = resolve(root, 'stories', registry.library, 'components');

  await rm(outDir, { recursive: true, force: true });
  await mkdir(outDir, { recursive: true });

  await Promise.all(
    entries.map((entry) => {
      const slug = entry.id.replace(registry.idPrefix, '');
      return writeFile(
        resolve(outDir, `${slug}.stories.tsx`),
        storySource({ entry, registry }),
      );
    }),
  );

  console.log(`generated ${entries.length} ${registry.library} component stories`);
}
